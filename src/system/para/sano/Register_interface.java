    private void btn_registrarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_registrarMousePressed
        // REGISTRAR DATOS
       String fnombres= nombresfield.getText();
       String apellidos= apellidosfield.getText();
       String celular= celularfield.getText();
       String user= userfield.getText();
       String pass= String.valueOf(passfield.getPassword());
       String sexo="Mujer";
       
       if (Radiobutton_hombre.isSelected())
       {
         sexo="Hombre";
       }
       
       if (verificar())
       {
           
           if(!checarusername(user)){
               PreparedStatement ps;
               ResultSet rs;
               String registrarquery="INSERT INTO `usuarios`(`fullname_usuario`, `nombre_usuario`"
                + ", `contra_usuario`, `celular_usuario`, `sexo_usuario`) VALUES (?,?,?,?,?)";
               System.out.println("si entró");
               try {
                   ps = bd_conexion.getConnection().prepareStatement(registrarquery);
                   
                   String fullname= fnombres+" "+apellidos;
       
                   ps.setString(1,fullname);
                   ps.setString(2,user);
                   ps.setString(3,pass);
                   ps.setString(4,celular);
                   ps.setString(5,sexo);
                   
            
                   System.out.println("entró");
                   
                   if(ps.executeUpdate() != 0)
                   {
                       JOptionPane.showMessageDialog(null,"La cuenta ha sido creada");
                       
                   }
                   else{
                       JOptionPane.showMessageDialog(null,"Error: Revisa bien la información");
                   }
                   
                   
               } catch (SQLException ex) {
                   Logger.getLogger(Register_interface.class.getName()).log(Level.SEVERE, null, ex);
               }
               
               
               
               
           }
       }
       
        Login_Admin_interface form = new Login_Admin_interface();
        form.setVisible(true);
        form.pack();
        form.setLocationRelativeTo(null);
       // close the current form (login form)
        this.dispose();
       
       
    }//GEN-LAST:event_btn_registrarMousePressed


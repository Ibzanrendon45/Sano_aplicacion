    //función para verificar los campos vacíos
    public boolean verificar(){
       String fnombres= nombresfield.getText();
       String apellidos= apellidosfield.getText();
       String celular= celularfield.getText();
       String user= userfield.getText();
       String pass= String.valueOf(passfield.getPassword());
       String confpass=String.valueOf(confpassfield.getPassword()); 
       
       //checar si los campos están vacíos
       if(fnombres.trim().equals("") || apellidos.trim().equals("") || celular.trim().equals("")
        || user.trim().equals("") || pass.trim().equals("") || confpass.trim().equals(""))
       {
           JOptionPane.showMessageDialog(null,"Uno o mas campos están vacíos","Campos vacíos",2);
           return false;
       }
       //checar si las contraseñas son iguales
       else if(!pass.equals(confpass))
       {
           JOptionPane.showMessageDialog(null,"Las contraseñas no coinciden","Confirme la contraseña",2);
           return false;
       }
       else
       {
       return true;
        
       }
    }
    
    //Función para checar si el usuario ya existe en la base de datos
    public boolean checarusername(String usuario){
       
        PreparedStatement st;
        ResultSet rs;
        boolean username_existe=false;
        
        String query = "SELECT * FROM `usuarios` WHERE `nombre_usuario` = ?";
        
        try {
            st = bd_conexion.getConnection().prepareStatement(query);
            
            st.setString(1,usuario);
            rs = st.executeQuery();
            
            if(rs.next())
            {
                username_existe = true;
                JOptionPane.showMessageDialog(null,"Él nombre de usuario ya existe, escoja otro","Nombre de usuario inválido",2);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Register_interface.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return username_existe;
    }
    
    
